    adr_path = git_repo / "docs" / "ADR-WEB.md"
    adr_path.write_text(
        "The product website is generated by `Website/build.py`.\n",
        encoding="utf-8",
    )
    repositories = RepositoryService(conn)
    repositories.register_repository(
        "akdb",
        RepositoryRegistration(repository_id="git", local_path=str(git_repo)),
    )
    repositories.register_repository(
        "akdb",
        RepositoryRegistration(repository_id="website", local_path=str(website_repo)),
    )
    KnowledgeService(conn).upsert_adr(
        "akdb",
        AdrInput(
            adr_id="ADR-WEB",
            title="Website boundary",
            status="accepted",
            source_uri=str(adr_path),
            raw_source=adr_path.read_text(encoding="utf-8"),
        ),
    )

    result = StalenessService(conn).find_status_quo_drifts("akdb", limit=20)

    references = {finding["reference"]: finding["check_type"] for finding in result["findings"]}
    assert "Website/build.py" not in references


