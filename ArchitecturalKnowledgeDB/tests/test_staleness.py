def test_compute_staleness_reports_review_recommended_for_newer_source_change(conn) -> None:
    add_project(conn, "akdb")
    adr = KnowledgeService(conn).upsert_adr("akdb", AdrInput(adr_id="ADR-0001", title="Old ADR"))
    ConsistencyService(conn).link("akdb", adr["item_uid"], "src/app.py", "governs")
    conn.execute(
        """
        INSERT INTO repositories(repository_id, project_id, local_path)
        VALUES ('repo', 'akdb', '.')
        """
    )
    conn.execute(
        """
        INSERT INTO git_file_history(
          file_history_uid, repository_id, project_id, file_path,
          first_seen_commit_hash, first_seen_at,
          last_changed_commit_hash, last_changed_at,
          change_count, metadata_json
        )
        VALUES ('fh1', 'repo', 'akdb', 'src/app.py', 'a', '2099-01-01T00:00:00Z', 'b', '2099-01-02T00:00:00Z', 2, '{}')
        """
    )

    result = StalenessService(conn).compute_project("akdb")

    assert result["generated"] == 1
    assert result["reports"][0]["status"] == "review_recommended"


def test_status_quo_drift_finds_missing_current_paths_and_symbols_without_git(
    conn,
    tmp_path: Path,
) -> None:
    add_project(conn, "akdb")
    repo = tmp_path / "repo"
    (repo / "src").mkdir(parents=True)
    (repo / "docs").mkdir()
    (repo / "src" / "current_service.py").write_text(
        "class CurrentService:\n    pass\n",
        encoding="utf-8",
    )
    adr_path = repo / "docs" / "ADR-0002.md"
    adr_path.write_text(
        "# ADR-0002: Current contracts\n\n"
        "The implementation uses `CurrentService` and still documents `MissingService` in `src/missing.py`.\n",
        encoding="utf-8",
    )
    RepositoryService(conn).register_repository(
        "akdb",
        RepositoryRegistration(repository_id="repo", local_path=str(repo)),
    )
    KnowledgeService(conn).upsert_adr(
        "akdb",
        AdrInput(
            adr_id="ADR-0002",
            title="Current contracts",
            status="accepted",
            source_uri=str(adr_path),
            raw_source=adr_path.read_text(encoding="utf-8"),
        ),
    )

    result = StalenessService(conn).find_status_quo_drifts("akdb", limit=20)

    assert result["mode"] == "status_quo"
    assert result["candidate_count"] == 2
    references = {finding["reference"]: finding["check_type"] for finding in result["findings"]}
    assert references["src/missing.py"] == "missing_path_reference"
    assert references["MissingService"] == "missing_symbol_reference"
    assert "CurrentService" not in references


def test_status_quo_drift_resolves_neighbor_repository_folder_alias(
    conn,
    tmp_path: Path,
) -> None:
    add_project(conn, "akdb")
    git_repo = tmp_path / "Git"
    website_repo = tmp_path / "Website"
    (git_repo / "docs").mkdir(parents=True)
    website_repo.mkdir()
