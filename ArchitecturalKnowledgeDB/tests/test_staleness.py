def test_status_quo_drift_can_be_persisted_and_called_through_mcp(conn, tmp_path: Path) -> None:
    add_project(conn, "akdb")
    repo = tmp_path / "repo"
    (repo / "docs").mkdir(parents=True)
    adr_path = repo / "docs" / "ADR-0003.md"
    adr_path.write_text("References `src/deleted.py`.\n", encoding="utf-8")
    RepositoryService(conn).register_repository(
        "akdb",
        RepositoryRegistration(repository_id="repo", local_path=str(repo)),
    )
    KnowledgeService(conn).upsert_adr(
        "akdb",
        AdrInput(
            adr_id="ADR-0003",
            title="Deleted path",
            status="accepted",
            source_uri=str(adr_path),
            raw_source=adr_path.read_text(encoding="utf-8"),
        ),
    )

    assert any(tool["name"] == "architectural_knowledge_db_find_status_quo_drifts" for tool in MCP_MANIFEST["tools"])
    assert any(tool["name"] == "architectural_knowledge_db_run_drift_check" for tool in MCP_MANIFEST["tools"])
    mcp_result = McpDispatcher(conn).dispatch(
        "architectural_knowledge_db_find_status_quo_drifts",
        {"project_id": "akdb", "limit": 10},
    )
    assert mcp_result["candidate_count"] == 1

    persisted = McpDispatcher(conn).dispatch(
        "architectural_knowledge_db_find_status_quo_drifts",
        {"project_id": "akdb", "limit": 10, "persist": True},
    )
    assert persisted["generated"] == 1
    reports = StalenessService(conn).list_reports("akdb", status_filter=["likely_stale"])
    assert reports[0]["target_type"] == "status_quo_drift"


def test_complete_drift_run_persists_all_status_quo_findings_even_when_return_limited(
    conn,
    tmp_path: Path,
) -> None:
    add_project(conn, "akdb")
    repo = tmp_path / "repo"
    (repo / "docs").mkdir(parents=True)
    adr_path = repo / "docs" / "ADR-0004.md"
    adr_path.write_text(
        "References `src/deleted_a.py` and `src/deleted_b.py`.\n",
        encoding="utf-8",
    )
    RepositoryService(conn).register_repository(
        "akdb",
        RepositoryRegistration(repository_id="repo", local_path=str(repo)),
    )
    KnowledgeService(conn).upsert_adr(
        "akdb",
        AdrInput(
            adr_id="ADR-0004",
            title="Deleted paths",
            status="accepted",
            source_uri=str(adr_path),
            raw_source=adr_path.read_text(encoding="utf-8"),
        ),
    )

    result = StalenessService(conn).run_drift_check("akdb", limit=1)

    assert result["mode"] == "complete_drift_run"
    assert result["modes_run"] == ["status_quo", "git_timeline"]
    assert result["status_quo"]["candidate_count"] == 2
    assert result["status_quo"]["generated_reports"] == 2
    assert len(result["status_quo"]["top_findings"]) == 1
    assert len(StalenessService(conn).list_reports("akdb", status_filter=["likely_stale"])) == 2
